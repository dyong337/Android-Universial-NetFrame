package com.chenjian.net.aes;

/**
 * AES加解密包装工具类
 * <p>
 * 作者： ChenJian
 * 时间： 2016.12.14 15:36
 */

public class AesUtil {


    public static String encryptToString(String encryptStr) {
        if (encryptStr == null) {
            return "";
        }
        return encryptStr;
    }

    public static String decryptToString(String decryptStr) {
        if (decryptStr == null) {
            return "";
        }
        return decryptStr;
    }
}
